// Laikmačio pertrauktis (kas 1 sek.)
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
    if (htim->Instance == TIM2) {
        // Mirksime PD12 (vietoje PG13 - TX aktyvumas)
        HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_12);

        // Išsiunčiame 1 baitą per SPI2 (vietoj SPI4)
        HAL_SPI_Transmit(&hspi2, &seq_data[seq_index], 1, 10);

        // Pereiname prie kito skaičiaus masyve
        seq_index++;
        if (seq_index >= 4) seq_index = 0;
    }
}

// SPI priėmimo pertrauktis
void HAL_SPI_RxCpltCallback(SPI_HandleTypeDef *hspi) {
    if (hspi->Instance == SPI3) {
        // Mirksime PD14 (vietoje PG14 - RX aktyvumas)
        HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_14);

        // Pakeliame vėliavėlę ekranui
        new_data_received = 1;

        // Vėl aktyvuojame SPI3 klausymąsi (Non-blocking mode)
        HAL_SPI_Receive_IT(&hspi3, &spi3_rx_byte, 1);
    }
}
