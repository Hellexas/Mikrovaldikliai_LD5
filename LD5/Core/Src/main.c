    // PB15 (MOSI) sujungti su PB14 (MISO)
    HAL_SPI_TransmitReceive(&hspi2, spi2_tx_data, spi2_rx_data, 4, HAL_MAX_DELAY);

    // Atvaizduojame testavimo rezultatus OLED ekrane
    sprintf(oled_buf, "TX: %d %d %d %d", spi2_tx_data[0], spi2_tx_data[1], spi2_tx_data[2], spi2_tx_data[3]);
